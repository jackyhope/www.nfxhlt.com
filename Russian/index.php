        <span>Описание компании</span>
        <p><span>Sino South</span> является крупномасштабной высокопрофессиональной организацией, занимающейся
            хэдхантингом и вопросами управленческого консалтинга. В штабе компании числится больше 1000 хэдхантеров и
            первоклассных консультантов. Головной офис находится в Чунцине. В Пекине, Шанхае, Шэньчжэне, Наньнине,
            Чанчуне, Ухане, Гуйяне и др. городах учреждены филиалы. Sino South уделяет особое внимание консультированию
            различных компаний по вопросам HR (людских ресурсов), хэдхантингу, проверке и оценке личностных качеств,
            программе помощи сотрудников (EAP) и другим услугам в области HR. Эксперт в Интернет-сетях, финансах,
            банковском деле, “Умной” обрабатывающей промышленности, недвижимости, лечении и медикаментах, а также в
            других областях RPO (аутсорсинг процесса рекрутмента) и консультировании по набору высококвалифицированных
            кадров.
            <br><br>
            Sino South - первая в Китае скоростная 24-часовая служба, ориентированная на результат и готовая
            предоставить своим клиентам высококачественные и эффективные услуги по набору кадров. Многие сотрудники -
            выпускники известных китайских университетов, обладают колоссальным опытом работы в области среднего и
            высшего уровня управления. Компания занимает лидирующие позиции по Китаю и имеет техническую поддержку со
            стороны знаменитых консультационных фирм по вопросам управления. Кроме того, Sino South обладает больше чем
            50 млн. талантов, объединенных в базе данныхх и в краткие сроки способна подобрать высококвалифицированного
            специалиста, примерно за 15 дней. На настоящий момент уже больше 30 тыс. предприятий выбирают услуги Sino
            South.</p>
